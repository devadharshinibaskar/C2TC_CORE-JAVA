package day1;

public class charDemo {

	public static void main(String[] args) {
		char ch='d';
		System.out.println(ch);
		
		char ch1=67;
		System.out.println(ch1);
		
		char ch2='\u00A7';
		System.out.println(ch2);
		
		char ch3='\u20AC';
		System.out.println(ch3);
		
		int a='D';
		System.out.println(a);
		

	}

}
